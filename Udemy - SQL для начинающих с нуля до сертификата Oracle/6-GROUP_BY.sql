SELECT JOB_ID, COUNT(*), TO_CHAR(HIRE_DATE, 'YYYY') year, COMMISSION_PCT, SUM(SALARY)
FROM EMPLOYEES
WHERE JOB_ID IN ('ST_CLERK', 'SA_REP', 'SH_CLERK')
  AND EMPLOYEE_ID > 115
GROUP BY JOB_ID, TO_CHAR(HIRE_DATE, 'YYYY'), COMMISSION_PCT
ORDER BY SUM(SALARY) DESC, year;

