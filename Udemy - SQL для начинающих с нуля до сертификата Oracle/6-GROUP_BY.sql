SELECT DISTINCT DEPARTMENT_ID
FROM EMPLOYEES;

SELECT COUNT(DISTINCT DEPARTMENT_ID)
FROM EMPLOYEES;

SELECT DEPARTMENT_ID, COUNT(*) total_employes, MIN(SALARY)
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
ORDER BY total_employes DESC;

SELECT JOB_ID,
       COUNT(*)    employes,
       ROUND(AVG(SALARY)),
       MIN(SALARY),
       MAX(SALARY),
       SUM(SALARY) total_salary
FROM EMPLOYEES
WHERE LENGTH(FIRST_NAME) > 4
  AND SALARY > 5000
--   AND COMMISSION_PCT IS NOT NULL
GROUP BY JOB_ID
ORDER BY employes DESC;

SELECT MAX(HIRE_DATE),
       MIN(FIRST_NAME),
       COUNT(*),
       ROUND(AVG(SALARY)),
       SUM(EMPLOYEE_ID)
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID;

SELECT TO_CHAR(HIRE_DATE, 'Month'), COUNT(*)
FROM EMPLOYEES
GROUP BY TO_CHAR(HIRE_DATE, 'Month')
ORDER BY COUNT(*) DESC;

