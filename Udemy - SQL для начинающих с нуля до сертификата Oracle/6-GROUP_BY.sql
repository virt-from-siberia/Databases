SELECT DEPARTMENT_ID, COUNT(*), ROUND(AVG(SALARY))
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
HAVING COUNT(*) > 3
   AND ROUND(AVG(SALARY)) > 5000
ORDER BY COUNT(*) DESC;

