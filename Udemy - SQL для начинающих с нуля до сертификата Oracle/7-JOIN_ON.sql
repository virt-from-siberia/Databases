SELECT FIRST_NAME, LAST_NAME, J.JOB_ID, START_DATE, END_DATE, E.EMPLOYEE_ID, J.EMPLOYEE_ID
FROM EMPLOYEES E
         JOIN JOB_HISTORY J ON (E.EMPLOYEE_ID = J.EMPLOYEE_ID);

