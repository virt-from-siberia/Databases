SELECT FIRST_NAME, LAST_NAME, SALARY, JOB_TITLE, DEPARTMENT_NAME
FROM EMPLOYEES EM
         JOIN JOBS JS ON (EM.JOB_ID = JS.JOB_ID)
         JOIN DEPARTMENTS DE ON (EM.DEPARTMENT_ID = DE.DEPARTMENT_ID)
WHERE JS.JOB_ID IN (SELECT JOB_ID FROM JOBS WHERE UPPER(JOB_TITLE) LIKE '%MANAGER%')
  AND SALARY > (SELECT AVG(SALARY) FROM EMPLOYEES);


