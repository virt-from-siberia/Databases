CREATE VIEW fin_emp AS
SELECT EMPLOYEE_ID, JOB_ID, SALARY
FROM EMPLOYEES;

SELECT *
FROM fin_emp;

CREATE VIEW fin_emp_2 AS
SELECT DEPARTMENT_NAME, SUM(SALARY) sum_salary
FROM EMPLOYEES EM
         JOIN DEPARTMENTS DE ON (DE.DEPARTMENT_ID = EM.DEPARTMENT_ID)
GROUP BY DEPARTMENT_NAME;

SELECT *
FROM fin_emp_2
ORDER BY fin_emp_2.SUM_SALARY DESC;










