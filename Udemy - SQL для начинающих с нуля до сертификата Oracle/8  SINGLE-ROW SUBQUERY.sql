FROM JOBS JS
         JOIN EMPLOYEES EM ON (JS.JOB_ID = EM.JOB_ID)
HAVING AVG(SALARY) = (SELECT MAX(AVG(SALARY)) FROM EMPLOYEES GROUP BY JOB_ID);


